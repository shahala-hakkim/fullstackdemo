class SwitchDemo{

public static void main(String args[]){

	String choice="hello";

	switch(choice){

	case "hi":System.out.println("You selected hi");break;
	case "hello":System.out.println("You selected hello");break;
	case "bye":System.out.println("You selected bye");break;
	default:System.out.println("wrong selection ");break;


}

}


}